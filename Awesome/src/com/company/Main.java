package com.company;

public class Main {

    public static void main(String[] args) {
        // For loop
        System.out.println("For Loop");
        for (int i=0;i<=100;i+=10){
            System.out.println(i);
        }
        // While Loop
        System.out.println("While loop");
        int j = 0;
        while (j<100){
            j++;
            System.out.println(j);
        }
    }
}
